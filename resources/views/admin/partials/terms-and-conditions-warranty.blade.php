<h3>Ceramic Coating FAQs:</h3>

<h4>What is ceramic coating?</h4>
<p>
    Ceramic coating is a liquid polymer applied to a vehicle's exterior surfaces. Once cured, it bonds with the factory
    paint, establishing a layer of protection and a hydrophobic surface.
</p>

<h4>How long does ceramic coating last?</h4>
<p>
    With proper maintenance, premium ceramic coatings can last several years. The exact duration varies based on
    environmental conditions, how often the vehicle is used, and how well it is maintained.
</p>

<h4>Does ceramic coating prevent scratches?</h4>
<p>
    While ceramic coating provides a protective layer, it isn't entirely scratch-proof. However, it can minimize light
    swirls and scatches, adding a level of protection against minor abrasions.
</p>

<h4>Can I wash my car regularly after applying a ceramic coating?</h4>
<p>
    Yes, regular washing is recommended to remove contaminants. Always use a pH-neutral soap and avoid abrasive
    materials. Always use touch free car washes or hand washing for best results! Stay away from any automated brush
    washes!
</p>

<h4>Does ceramic coating require special maintenance?</h4>
<p>
    Annual inspections and using the right cleaning products will help maintain its shine and protection. Avoid using
    harsh chemicals or brushes that might damage the coating.
</p>

<h4>What is warranty that is offered with ceramic coating?</h4>
<p>
    Ceramic coating warranty primarily addresses manufactures production defects and any discrepancies during
    installation. It's essential to understand that coatings efficacy largely depends on its care. For optimal longevity
    and durability, an annual maintenance by an approved installer is strongly recommended.
</p>

<h3>PPF (Paint Protection Film) FAQs:</h3>

<h4>What is Paint Protection Film (PPF)?</h4>
<p>
    PPF is a transparent, thin film applied to a vehicle's exterior to protect it from scratches, rock chips, and
    environmental contaminants.
</p>

<h4>How does PPF differ from ceramic coating?</h4>
<p>
    While both offer protection, PPF is more focused on physical protection against chips and scratches. Ceramic
    coating, on the other hand, offers protection against contaminants, UV damage, and provides a hydrophobic effect.
</p>

<h4>Does PPF turn yellow over time?</h4>
<p>
    Quality PPFs are designed to resist yellowing. However, over extended periods and under harsh conditions, some
    slight discolouration might occur. Regular maintenance can help prevent this.
</p>

<h4>How long does PPF last?</h4>
<p>
    Depending on the quality of the film, environmental conditions, and maintenance, PPF can last several years.
    Periodic inspections are recommended to check for signs of wear or damage.
</p>

<h4>Is PPF completely resistant to stone chips and scratches?</h4>
<p>
    PPF provides an enhanced layer of defence against stone chips, road debris, and minor abrasions, significantly
    reducing the risk of damage to your vehicle's paint. However, it's important to note that PPF isn't "bulletproof."
    In cases of high-speed impacts or sharp, forceful debris, there's still a possibility of stone chips penetrating
    through. Think of PPF as a highly effective shield; it will take the brunt of many impacts and preserve your
    vehicle's aesthetics, but under extreme conditions, no shield is absolute.
</p>

<h4>Can PPF be removed? Will it damage the paint?</h4>
<p>
    PPF is designed for safe removal by professionals. When done correctly, removal will not damage the underlying
    paint. In fact, the paint beneath will typically look as vibrant as the day the film was applied.
</p>

<h4>What is covered under warranty with ppf?</h4>
<p>
    PPF's warranty typically covers defects in materials and workmanship, including yellowing, staining, and cracking.
    However, it doesn't cover damages from accidents, misuse, or wear from abrasive materials (stones on the road ).
    Always consult with the official installer for any warranty concerns or any coverage details.
</p>
