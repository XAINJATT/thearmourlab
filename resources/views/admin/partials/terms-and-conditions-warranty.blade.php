<h3>AFTERCARE INSTRUCTIONS & RULES:</h3>

<p>
    Ceramic coating amongst other things is designed to protect your factory clear coat. It is tremendously effective at
    doing so. However, it is not bulletproof and will require basic maintenance. With simple care your coating will
    reward you for many, many years to come!
</p>

<h4>THINGS YOU MUST AVOID DURING CURING (7-10 DAYS):</h4>
<ol>
    <li>Curing time is 7-10 days depending on the environmental conditions such as humidity and temperature.</li>
    <li>DO NOT wash your car with hard water. It can leave calcium spots all over the vehicle!</li>
    <li>Tree sap. Avoid parking under trees that shed!</li>
    <li>Allowing bird poo, sap, pollen to sit on your car and bake in the sun.</li>
    <li>Washing or abrading the paint.</li>
</ol>

<h4>WASHING YOUR CAR AT HOME:</h4>

<p>
    Ceramic coating is extremely slick, this means most things will dissolve and be pulled off by the ceramic coatings
    self-cleaning effects. Minimizing rubbing, (especially with a dry towel) on the coating will ensure its longevity.
    Use any mild cleaner or wet towel to remove bird droppings, bugs and tree sap. Followed by wiping it off with a soft
    edgeless microfibre towel. DO NOT USE DISH SOAP! Always make sure your soap is a PH Neutral car wash soap! YES you
    can still hand wax your car if you feel the need to!
</p>

<h4>WASHING SCHEDULE:</h4>

<p>
    For best results, the car should be washed on a weekly basis to avoid excessive contamination build up! Use only PH
    neutral soaps or mild car wash soaps. Avoid any touch car washes (brush washes) and high PH detergent style cleaners
    as this will void the warranty! Always wash from top down leaving the dirtiest section for last to avoid cross
    contamination.
</p>

<h4>WASHING METHODS:</h4>

<h3>STANDARD HOME WASHING:</h3>

<p>
    Use a foam gun (optimal) or microfibre wash mitt to soap down the car. Rinse it off with a pressure washer for best
    results. If the car is moderately dirty use a plushy wash mitt and two bucket method! Dry with clean microfibre
    towels.
</p>

<h3>SPRAY N' WASH SHELF CAR WASHES:</h3>

<p>
    Rinse entire car down with power wash. After Select the high pressure soap. Soap the entire car down (never use the
    provided brush!) if the car is dirty, use a plushy microfibre wash mitt to agitate the dirt. Rinse off with high
    pressure rinse, Dry with clean microfibre towel.
</p>

<h3>TOUCH-LESS CAR WASHES:</h3>

<p>
    Touch-less car washes only! Never use a car wash with brush washes! Always go for the cheapest option when selecting
    the car wash. No need for all the crazy gizmos and soaps.
</p>

<h4>DRYING:</h4>
<p>
    Always dry completely and never leave the "air" to dry your car. Tap water contains minerals that may leave deposits
    creating water spots. Only use quality microfibre towels for best results and avoid aggressive wiping, allow the
    material to absorb the water.
</p>

<h4>SPOT REMOVAL:</h4>
<p>
    Never use aggressive products to polish the coating or remove surface contamination. Never use excessive force to
    remove spots. Bird droppings, tar, sap, bugs etc. Should be removed as soon as possible to avoid temporary staining
    or hardening on the coating. Any staining left behind from bird droppings will break down over a few days without
    affecting the coating. Bug guts, sap, bird droppings and tar are very acidic and can etch the coating if not taken
    care of right away.
</p>

<h4>AFTER WASHING:</h4>
<p>
    It is always recommended to use any SI02 spray to maintain the coating. DO NOT USE POLISH as this will potentially
    remove the coating and void warranty!
</p>

<h4>AUTOMATED WASHING:</h4>
<p>
    Always go to a touch-lee wash! Using an automated car wash with brushes will instantly void warranty and dull the
    coating! Ceramic coating takes 7-10days to achieve full hardness. Please handle with care and do not wash the
    vehicle for the first 7-10 days!
</p>

<h4>LEATHER AND FABRIC MAINTENANCE:</h4>
<p>
    For best results, all leather / vinyl surfaces should be cleaned bi-weekly with a PH neutral cleaner and leather
    protectant to avoid contaminant build-up and replenish the natural oils. Vacuum carpets regularly and use carpet and
    fabric cleaners when necessary. Immediately wipe any spills on leather, vinyl, fabric or suede.
</p>

<h4>ANNUAL INSPECTION:</h4>
<p>
    Annual inspections are the recommended process for ensuring the coating is maintained and care for the vehicle is
    properly executed. To ensure the durability and high performance of your ceramic coating, it's crucial to schedule
    an annual inspection with an approved installer, ideally within 30 days before the anniversary of your last service.
    While ceramic coatings offer robust protection, they can degrade due to factors like harsh weather, road salts, and
    contaminants. Regular check-ups by trained professionals ensure that your vehicle maintains its glossy appearance
    and is consistently protected. Missing these inspections might lead to premature coating degradation. For your
    benefit, keep a record of all maintenance and inspections, as this could be essential for warranty claims or future
    vehicle resale.
</p>
